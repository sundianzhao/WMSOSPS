$.QueryStringToJSON = function (json) {
    if (json == "") {
        return;
    }
    var pairs = json.split('&');
    var result = {};
    pairs.forEach(function (pair) {
        pair = pair.split('=');
        result[pair[0]] = decodeURIComponent(pair[1] || '');
    });
    return JSON.parse(JSON.stringify(result));
}
